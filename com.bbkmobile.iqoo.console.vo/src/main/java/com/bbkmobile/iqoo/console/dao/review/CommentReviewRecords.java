package com.bbkmobile.iqoo.console.dao.review;

import java.util.Date;

import com.bbkmobile.iqoo.console.dao.appinfo.AppComment;
import com.bbkmobile.iqoo.console.dao.login.BbkAdminUser;

public class CommentReviewRecords {
      
    private Long id;
    private BbkAdminUser bbkAdminUser=new BbkAdminUser();
    private AppComment appComment = new AppComment();
    private Character reviewStatus;
    private String reviewOpinion;
    private Date reviewDate;
    public Long getId() {
        return this.id;
    }
    public void setId(Long id) {
        this.id = id;
    }
    
    public BbkAdminUser getBbkAdminUser() {
        return this.bbkAdminUser;
    }
    public void setBbkAdminUser(BbkAdminUser bbkAdminUser) {
        this.bbkAdminUser = bbkAdminUser;
    }
    public AppComment getAppComment() {
        return this.appComment;
    }
    public void setAppComment(AppComment appComment) {
        this.appComment = appComment;
    }
    public Character getReviewStatus() {
        return this.reviewStatus;
    }
    public void setReviewStatus(Character reviewStatus) {
        this.reviewStatus = reviewStatus;
    }
    public String getReviewOpinion() {
        return this.reviewOpinion;
    }
    public void setReviewOpinion(String reviewOpinion) {
        this.reviewOpinion = reviewOpinion;
    }
    public Date getReviewDate() {
        return this.reviewDate;
    }
    public void setReviewDate(Date reviewDate) {
        this.reviewDate = reviewDate;
    }
    
    
}
